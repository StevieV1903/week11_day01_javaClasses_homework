public class Calculator {

    public int addingNumbers(int num_1, int num_2) {
        int total = (num_1 + num_2);
        return total;
    }

    public int subtractingNumbers(int num_1, int num_2) {
        int total = (num_1 - num_2);
        return total;
    }

    public int multiplyingNumbers(int num_1, int num_2) {
        int total = (num_1 * num_2);
        return total;
    }

    public double dividingNumbers(double num_1, double num_2) {
        double total = (num_1 / num_2);
        return total;
    }
}
